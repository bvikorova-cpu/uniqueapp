
-- Shadow Arena AI Credits System
CREATE TABLE IF NOT EXISTS public.shadow_arena_credits (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL UNIQUE,
  credits_remaining INTEGER NOT NULL DEFAULT 5,
  total_credits_purchased INTEGER NOT NULL DEFAULT 5,
  last_used_at TIMESTAMPTZ,
  created_at TIMESTAMPTZ NOT NULL DEFAULT now(),
  updated_at TIMESTAMPTZ NOT NULL DEFAULT now()
);

ALTER TABLE public.shadow_arena_credits ENABLE ROW LEVEL SECURITY;

CREATE POLICY "Users view own shadow credits" ON public.shadow_arena_credits FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "Users insert own shadow credits" ON public.shadow_arena_credits FOR INSERT WITH CHECK (auth.uid() = user_id);
CREATE POLICY "System updates shadow credits" ON public.shadow_arena_credits FOR UPDATE USING (true);

