-- Storage bucket for nightmare avatars
INSERT INTO storage.buckets (id, name, public) VALUES ('shadow-nightmare-avatars', 'shadow-nightmare-avatars', true)
ON CONFLICT (id) DO NOTHING;

CREATE POLICY "Public read nightmare avatars" ON storage.objects FOR SELECT USING (bucket_id = 'shadow-nightmare-avatars');
CREATE POLICY "Users upload nightmare avatars" ON storage.objects FOR INSERT WITH CHECK (bucket_id = 'shadow-nightmare-avatars' AND auth.uid()::text = (storage.foldername(name))[1]);
CREATE POLICY "Users delete own nightmare avatars" ON storage.objects FOR DELETE USING (bucket_id = 'shadow-nightmare-avatars' AND auth.uid()::text = (storage.foldername(name))[1]);

-- Stripe-driven topup transactions for Shadow Arena AI credits
CREATE TABLE IF NOT EXISTS public.shadow_credit_purchases (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  user_id UUID NOT NULL,
  credits_purchased INTEGER NOT NULL,
  amount_eur NUMERIC(10,2) NOT NULL,
  stripe_session_id TEXT,
  status TEXT NOT NULL DEFAULT 'pending',
  created_at TIMESTAMPTZ NOT NULL DEFAULT now()
);
ALTER TABLE public.shadow_credit_purchases ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Users view own credit purchases" ON public.shadow_credit_purchases FOR SELECT USING (auth.uid() = user_id);
CREATE POLICY "System manages credit purchases" ON public.shadow_credit_purchases FOR ALL USING (true);

-- Trigger to update updated_at
CREATE OR REPLACE FUNCTION public.update_shadow_credits_timestamp()
RETURNS TRIGGER LANGUAGE plpgsql SET search_path = public AS $$
BEGIN
  NEW.updated_at = now();
  RETURN NEW;
END;
$$;

DROP TRIGGER IF EXISTS shadow_credits_updated ON public.shadow_arena_credits;
CREATE TRIGGER shadow_credits_updated BEFORE UPDATE ON public.shadow_arena_credits FOR EACH ROW EXECUTE FUNCTION public.update_shadow_credits_timestamp();
